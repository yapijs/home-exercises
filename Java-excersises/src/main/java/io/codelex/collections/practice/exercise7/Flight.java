package io.codelex.collections.practice.exercise7;

public class Flight {
    private final String start;
    private final String end;

    public Flight(String start, String end) {
        this.start = start;
        this.end = end;
    }

    public String getStart() {
        return start;
    }

    public String getEnd() {
        return end;
    }
}
